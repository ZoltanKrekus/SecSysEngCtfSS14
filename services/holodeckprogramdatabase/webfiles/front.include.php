
<h2>Welcome to the Holodeck Program Database</h2>

<p>With this service you can store confident holodeck access tokens and protect them with a key. Do not forget your key - your holodeck access information will be lost otherwise!</p>

        <h3>Functionality:</h3>
<p>Below is a short description of the service to make you familiar with it.</p>

<p>
You can add access information for holodecks and retrieve them later with the given key.
You have to provide the name of the holodeck and a secret key for later retrieval of the holodeck access token. 
For easier use, a list of all holodeck names is provides for every ship member.

To let you remember your key easier it is displayed masked with question marks (?).
</p>


